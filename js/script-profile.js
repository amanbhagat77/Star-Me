function onSignIn(googleUser){

	var profile = googleUser.getBasicProfile();
	$(".g-signin2").css("display","none");
	$(".container").css("display","block");
	$("#pic").attr('src',profile.getImageUrl());
	$("#name").text(profile.getName());


}

